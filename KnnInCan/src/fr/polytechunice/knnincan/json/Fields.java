package fr.polytechunice.knnincan.json;

public class Fields {

	Float stop_lat;
	Float stop_lon;
	Integer stop_id;
	
	
	public Fields() {
		super();
	}
	
	public Float getStop_lat() {
		return stop_lat;
	}
	public void setStop_lat(Float stop_lat) {
		this.stop_lat = stop_lat;
	}
	public Float getStop_lon() {
		return stop_lon;
	}
	public void setStop_lon(Float stop_lon) {
		this.stop_lon = stop_lon;
	}
	public Integer getStop_id() {
		return stop_id;
	}
	public void setStop_id(Integer stop_id) {
		this.stop_id = stop_id;
	}

	
	
}
